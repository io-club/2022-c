#include <stdio.h>
int main(int argc, char *argv[]) {
  int arr[10][10] = {};
  for (int i = 0; i < 10; i++) {
    for (int j = 0; j < 10; j++) {
      printf("%d ", arr[i][j]);
    }
    printf("\n");
  }
  return 0;
}

